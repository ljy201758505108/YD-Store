package com.yanda.bean;

public class Order {
	int id;
	
	String oid;
	
	String uname;
	
	String tel;
	
	String addrBig;
	
	String addrSmall;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getOid() {
		return oid;
	}

	public void setOid(String oid) {
		this.oid = oid;
	}

	public String getUname() {
		return uname;
	}

	public void setUname(String uname) {
		this.uname = uname;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getAddrBig() {
		return addrBig;
	}

	public void setAddrBig(String addrBig) {
		this.addrBig = addrBig;
	}

	public String getAddrSmall() {
		return addrSmall;
	}

	public void setAddrSmall(String addrSmall) {
		this.addrSmall = addrSmall;
	}
	
	

}
